"""
CBD 动态道路收费系统 — 全局配置
路网：10×10 网格, 3600m×3600m, 每格 400m
CBD 收费区：中央 2000m×2000m, 外围 800m 绕行环线
"""
import math

# ==========================================
# 仿真全局参数
# ==========================================
SIM_STEPS = 10800              # 3小时早高峰 (秒)
INITIAL_BALANCE = 100.0        # 账户初始余额 (元)

# ==========================================
# 路网文件
# ==========================================
NET_FILE = "cbd_grid.net.xml"
ROUTE_FILE = "routes.rou.xml"
PARK_FILE = "parks.add.xml"

# ==========================================
# CBD 收费区地理围栏 (2km × 2km, 居中)
# 路网范围 0~3600m, CBD 范围 800~2800m
# 外围保留 800m 宽的免费绕行环线
# ==========================================
CBD_X_MIN, CBD_X_MAX = 800.0, 2800.0
CBD_Y_MIN, CBD_Y_MAX = 800.0, 2800.0

# ==========================================
# 基于距离的收费参数
# ==========================================
BASE_RATE_PER_KM = 2.0          # 基础费率 (元/公里)
CHARGE_INTERVAL_METERS = 500.0  # 每 500 米结算一次
TOLL_THRESHOLD_HIGH = 5.0       # 触发绕行博弈的敏感费率 (元/公里)

# ==========================================
# 动态定价 — Logistic 曲线
# 拐点 = 25 辆, 斜率 = 0.20, 范围: 2.0 ~ 10.0 元/公里
# 校准依据: CBD 实际峰值在 20~50 辆, 拐点设在 25 辆使价格在真实区间内充分响应
# ==========================================
def get_toll_fee_per_km(vehicle_count):
    congestion_multiplier = 1 + 4 / (1 + math.exp(-0.20 * (vehicle_count - 25)))
    return BASE_RATE_PER_KM * congestion_multiplier

# ==========================================
# 绕行博弈参数
# ==========================================
# 时间价值范围 (元/秒, 折合时薪 72~288 元)
VOT_MIN, VOT_MAX = 0.02, 0.08
# 绕行额外耗时范围 (秒)
# 对于 2km×2km 的 CBD, 绕行外环仅多 1~3 分钟 (60~180 秒)
# 原来的 3~7 分钟导致绕行成本 >> 过路费节省, 使几乎没人绕行
DETOUR_TIME_MIN, DETOUR_TIME_MAX = 60, 180
# 预估穿越 CBD 的平均距离 (公里)
# 2km×2km CBD, 对角线约 2.83km, 直穿约 1.5~2km
EXPECTED_CBD_DISTANCE_KM = 1.5

# 路权惩罚换算系数: 元/km → 等效秒/km
# 推导: 绕行路径比直穿CBD约多1600m, 在30km/h下多192秒
# 要让SUMO路由器选绕行, CBD的惩罚必须使: penalized_cbd_time > detour_time
# 即: actual_tt + fee × factor × len/1000 > 432s
# 解出: factor > (432 - 240) / (10 × 2) = 9.6, 取20留出余量确保5元/km时也触发
TOLL_TO_TIME_FACTOR = 20

